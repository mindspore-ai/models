class StandardScaler:
    def __init__(self, mean, std):
        self.mean = mean
        self.std = std

    def transform(self, data):
        return (data - self.mean) / self.std

    def get_mean(self):
        return self.mean

    def get_std(self):
        return self.std


def load_dataset():
    data = {}
    for category in ['train', 'val', 'test']:
        cat_data = np.load(os.path.join('data/METR-LA', category + '.npz'))
        data['x_' + category] = cat_data['x']
        data['y_' + category] = cat_data['y']
    scaler = StandardScaler(mean=data['x_train'][..., 0].mean(), std=data['x_train'][..., 0].std())
    # Data format
    for category in ['train', 'val', 'test']:
        data['x_' + category][..., 0] = scaler.transform(data['x_' + category][..., 0])
        data['y_' + category][..., 0] = scaler.transform(data['y_' + category][..., 0])
    data['scaler'] = scaler

    return data


class GetDatasetGenerator():
    def __init__(self, xs, ys, batch_size, pad_with_last_sample=True, shuffle=False):
        np.random.seed(58)
        self.batch_size = batch_size
        self.current_ind = 0
        if pad_with_last_sample:
            num_padding = (batch_size - (len(xs) % batch_size)) % batch_size
            x_padding = np.repeat(xs[-1:], num_padding, axis=0)
            y_padding = np.repeat(ys[-1:], num_padding, axis=0)
            xs = np.concatenate([xs, x_padding], axis=0)
            ys = np.concatenate([ys, y_padding], axis=0)
        self.size = len(xs)
        self.num_batch = int(self.size // self.batch_size)
        if shuffle:
            permutation = np.random.permutation(self.size)
            xs, ys = xs[permutation], ys[permutation]
        self.__data = xs
        self.__label = ys

    def __getitem__(self, index):
        output = np.stack([self.__data[index], self.__label[index]], axis=0)
        target = self.__label[index]
        return (output, target)

    def __len__(self):
        return len(self.__data)


def get_loader_dataset(config):
    data = load_dataset()
    dataset_generator = GetDatasetGenerator(data['x_train'], data['y_train'], batch_size=64, shuffle=True)
    if config.distributed:
        rank_id = get_rank()
        rank_size = get_group_size()
        dataset = ds.GeneratorDataset(source=dataset_generator, column_names=["data", "label"],
                                      shuffle=True, num_parallel_workers=config.num_workers,
                                      num_shards=rank_size, shard_id=rank_id)
    else:
        dataset = ds.GeneratorDataset(dataset_generator, ["data", "label"], shuffle=True)
    dataset = dataset.batch(batch_size=64, num_parallel_workers=config.num_workers)
    return dataset


